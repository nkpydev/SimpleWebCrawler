import sys
import os
import time

platform = ''

def get_platform():
    try:
        if sys.platform.lower() == "win32":        
            print("Spydr now starting for Windows Platform!!")
            platform = "Windows"
            
        elif sys.platform.lower() == "darwin":
            print("Spydr now starting on Mac Platform!")
            platform = "Mac"
        
        else:
            print("Spydr now starting on Linux Platform!")
            platform = "Linux"
    except Exception as e:
        print(f"Error..\n{e}\n..occured, while finding platform...")
        time.sleep(2)
        print("\n.")
        time.sleep(2)
        print("\n.")
        print("Exiting now!")
        sys.exit(1)
    finally:
        return platform


